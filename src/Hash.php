    public function __construct()
    {
        $this->symbol = range("a", "z");
        $this->symbol = array_merge($this->symbol, range("A", "Z"));
        $this->symbol = array_merge($this->symbol, $this->all_char, range(0, 9), $this->emoji);
        $this->symbol_code = range(0, count($this->symbol) - 1);
    }

    /**
     * @param string $string
     * @return string
     */
    public function generate(string $string): string
    {
        return md5($this->numerical($string));
    }

    public function numerical(string $string): string
    {
        $exp = str_replace($this->symbol,$this->symbol_code, $string);
        return ($exp);
    }

    /**
     * @param string $string
     * @param string $hashed
     * @param bool $numerical
     * @return bool
     */
    public function checkHash(string $string, string $hashed, ?bool $numerical = false): bool
    {
        if ($numerical){
            $status = $this->numerical($string) == $hashed;
        }else{
            $status = $this->generate($string) == $hashed;
        }

        return $status;
    }

